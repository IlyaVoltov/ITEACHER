number = int(input("Введите число: "))

factorial = 1
for i in range(1, number + 1):
    factorial *= i
    if factorial == number:
        print(f"{number} — это факториал числа {i}")
        break
    elif factorial > number:
        print(f"{number} не является факториалом никакого числа")
        break


'''
Объяснение:
Инициализация переменных:

factorial = 1: начальное значение для подсчета факториала.

Цикл for:

Цикл перебирает числа от 1 до введенного числа.
На каждой итерации вычисляется текущий факториал.

Проверка в цикле:

Если текущий factorial равен введенному числу, программа выводит результат и прерывает цикл.
Если текущий factorial превышает введенное число, программа сообщает, что число не является факториалом, и прерывает цикл.

Почему так работает?
Цикл идет строго по числам, пока либо не найдет факториал, либо не превысит значение введенного числа. Это эффективно и просто.

'''